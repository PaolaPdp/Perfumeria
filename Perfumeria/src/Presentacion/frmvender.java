    private void calcprecioTotal(){
        
       Double pre=Double.parseDouble(txtpreciouni.getText()); 
       Double cant=Double.parseDouble(txtcantidad.getText());
       Double tot=cant*pre;
       this.txtpreciototal.setText(String.valueOf(tot));
    }
    public void calcimportecompra(){
        Double sumatoria;
        Double sumatoria1=0.0;
        int totalrow=tabladetalles.getRowCount();
        totalrow-=1;
        for(int i =0;i<=(totalrow);i++){
            sumatoria=Double.parseDouble(String.valueOf(tabladetalles.getValueAt(i,3)));
            sumatoria1+=sumatoria;            
        }
        Double imp=sumatoria1+(sumatoria1*0.18);
        txtimportecompra.setText(String.valueOf(imp));
    }
    public void calcimportedescuento(){
        vperfume vperfume=new vperfume();
        
        Double sumatoria;
        Double sumatoria1=0.0;
        
        int totalrow=tabladetalles.getRowCount();
        totalrow-=1;
        for(int i=0;i<=totalrow;i++){
            sumatoria=Double.parseDouble(String.valueOf(tabladetalles.getValueAt(i,2)));
            sumatoria1+=sumatoria;
        }
        String importecompra=(txtimportecompra.getText());
        if(sumatoria1<=5){ 
            Double porc =(vperfume.porcentaje1/100);
            Double descporc=Double.parseDouble(importecompra)*porc;
            txtimportedescuento.setText(String.valueOf(descporc));
        }
        if(sumatoria1>=6 && sumatoria1<=10){
            Double porc =(vperfume.porcentaje2/100);
            Double descporc=Double.parseDouble(importecompra)*porc;
            txtimportedescuento.setText(String.valueOf(descporc));
        }
        if(sumatoria1>=11 && sumatoria1<=15){
            Double porc =(vperfume.porcentaje3/100);
            Double descporc=Double.parseDouble(importecompra)*porc;
            txtimportedescuento.setText(String.valueOf(descporc));
        }
        if(sumatoria1>=16){
            Double porc =(vperfume.porcentaje4/100);
            Double descporc=Double.parseDouble(importecompra)*porc;
            txtimportedescuento.setText(String.valueOf(descporc));
        }
    }
    
    private void calcimportepagar(){
       String impcompra= txtimportecompra.getText();
       String impdescuento= txtimportedescuento.getText();
       Double importepagar=(Double.parseDouble(impcompra))+(Double.parseDouble(impdescuento));
       txtimportepagar.setText(String.valueOf(importepagar));
       
       int importeobsequi = vperfume.importeMinimoObsequiable ;
       
       if(importepagar >= importeobsequi){
           JOptionPane.showMessageDialog(null, "SE GANO : "+vperfume.obsequio);
       }
       
    }
    
